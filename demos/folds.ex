-- Determine output mode
--------------------------------------------------------------------------------
with define IUPWB
--------------------------------------------------------------------------------
-- The source
--------------------------------------------------------------------------------
include sequence.e
sequence list = range(10)
--------------------------------------------------------------------------------
-- Start output
--------------------------------------------------------------------------------
include io.e
write("<h1>The list</h1>")
writel(list)
--------------------------------------------------------------------------------
-- Define the fold,map and filter functions
--------------------------------------------------------------------------------
function sq(atom a,sequence b)
    return b&a*a
end function
--------------------------------------------------------------------------------
function add(atom a, atom b)
    return b+a
end function
include boolean.e
--------------------------------------------------------------------------------
function even(atom a,sequence b)
    return iif(remainder(a,2),b,b&a)
end function
--------------------------------------------------------------------------------
function gt(atom a,atom b)
    return a>b
end function
--------------------------------------------------------------------------------
write("<h2>Squares</h2>")
--------------------------------------------------------------------------------
write("foldr(list,\"sq\")-> ")
writel(foldr(list,"sq"))
--------------------------------------------------------------------------------
write("<h2>Sum of the elements</h2>")
writefln(foldr(list,"add",0),"foldr(list,\"add\",0)-> %d")
--------------------------------------------------------------------------------
writeln("<h2>The even elements</h2>")
write("foldr(list,\"even\")-> ")
writel(foldr(list,"even"))
writeln("<h2>The elements > 3</h2><p>filter(list,\"gt\",3)->")
writel(filter(list,"gt",3))
--------------------------------------------------------------------------------
display("Sequences: Showing how foldr does maps and filters, too!")
