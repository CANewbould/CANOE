-- Uses the default output mode - STDOUT
--------------------------------------------------------------------------------
-- Define the source
--------------------------------------------------------------------------------
constant s = "I am the way, the Truth and the Light."
--------------------------------------------------------------------------------
-- Start output
--------------------------------------------------------------------------------
include io.e -- for 'writefln'
writefln(s,"The source text: %s")
--------------------------------------------------------------------------------
-- Output results
--------------------------------------------------------------------------------
include sequence.e -- for 'foldr','toLower','toUpper'
writefln(toLower(s),"Lower case: %s")
writefln(toUpper(s),"Upper case: %s")
writefln(foldr(s,"cons"),"Reversed: %s")
--------------------------------------------------------------------------------
-- Testing split function
--------------------------------------------------------------------------------
constant sp = split(s," ,.")
writeln("Words:")
for i = 1 to length(sp) do
    writef(sp[i],"/%s")
end for
writeln("/")
--------------------------------------------------------------------------------
-- Testing s2a function
--------------------------------------------------------------------------------
writefln(s2a("12.786"),"\ns2a(\"12.786\")->%g")
writefln(s2a("-99.345"),"s2a(\"-99.345\")->%g")
writefln(s2a("-0.00005"),"s2a(\"-0.00005\")->%g")
--------------------------------------------------------------------------------
display("Testing 'toLower', 'toUpper', 'reverse', 'split' and 's2a'")
